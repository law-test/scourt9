def today() -> str:
    return datetime.now(ZoneInfo("Asia/Seoul")).date().isoformat()


def load_rep_rows() -> dict[str, dict[str, str | None]]:
    rows: dict[str, dict[str, str | None]] = {}
    for line in REP_BLOCK.strip().splitlines():
        unit_id, rep, *rest = line.split("|")
        rows[unit_id] = {"rep": rep, "trap": rest[0] if rest and rest[0] else None}
    return rows


def question_no(unit_id: str) -> int:
    match = re.search(r"-g4-(\d+)-", unit_id)
    if not match:
        raise ValueError(f"cannot parse question number: {unit_id}")
    return int(match.group(1))


def atom_id(unit_id: str) -> str:
    match = re.search(r"-g4-(\d+)-(.+)$", unit_id)
    if not match:
        raise ValueError(f"cannot parse atom id: {unit_id}")
    no = int(match.group(1))
    label = match.group(2)
    code = LABEL_CODE.get(label, label)
    return f"bupmusa-2025-deposit-q{no:02d}-{code}"


def validate(items: list[dict]) -> None:
    bad_tokens = ["위 ", "위의", "③", "옳은 것은", "옳지 않은 것은", "?"]
    for item in items:
        rep = item["rep"]
        for token in bad_tokens:
            if token in rep:
                raise ValueError(f"bad token {token!r} in {item['atomId']}: {rep}")
        if re.search(r"[가-힣][A-Za-z0-9가-힣]{18,}", rep):
            raise ValueError(f"suspicious spacing in {item['atomId']}: {rep}")


def build() -> Path:
    queue = json.loads(QUEUE_PATH.read_text(encoding="utf-8"))
    reps = load_rep_rows()
    queue_items = queue["items"]
    missing = sorted({item["unitId"] for item in queue_items} - set(reps))
    extra = sorted(set(reps) - {item["unitId"] for item in queue_items})
    if missing or extra:
        raise ValueError(f"coverage mismatch missing={missing} extra={extra}")

    atoms = []
    for item in queue_items:
        unit_id = item["unitId"]
        qno = question_no(unit_id)
        basis = QUESTION_BASIS[qno]
        row = reps[unit_id]
        source_verdict = item["originalVerdict"]
        is_x_source = source_verdict == "X"
        atoms.append(
            {
                "atomId": atom_id(unit_id),
                "sourceUnitId": unit_id,
                "sourceFamily": item["sourceFamily"],
                "source": item["source"],
                "year": item["year"],
                "round": item["round"],
                "subject": item["subject"],
                "no": item["no"],
                "unitType": item["unitType"],
                "unitLabel": item["unitLabel"],
                "sourceQuestionType": item["sourceQuestionType"],
                "sourceVerdict": source_verdict,
                "currentVerdict": "O",
                "rep": row["rep"],
                "a": "O",
                "basisType": basis["basisType"],
                "basisRef": basis["basisRef"],
                "why": basis["why"],
                "sourceStatement": item["rawStatement"],
                "sourceTrap": row["trap"] if is_x_source else None,
                "xDependsOn": row["rep"] if is_x_source else None,
                "reviewedAt": today(),
                "currentLawCheckedAt": today(),
            }
        )

    validate(atoms)
    doc = {
        "schema": "legal-scrivener/completed-atoms-by-subject/v1",
        "sourceFamily": "법무사시험",
        "examId": queue["examId"],
        "year": queue["year"],
        "round": queue["round"],
        "subject": "공탁법",
        "updatedAt": today(),
        "atomPrinciple": "docs/atom_원칙_v001.md",
        "sourceQueue": str(QUEUE_PATH),
        "sourceCount": len(queue_items),
        "atomCount": len(atoms),
        "policy": {
            "sourceStatement": "문제 원문 지문은 보존한다.",
            "rep": "화면 노출용 atom은 O인 자기완결식 법리 문장으로 작성한다.",
            "xHandling": "출제 원문이 X인 경우에도 rep는 올바른 O 법리로 정정하고 sourceTrap에 함정을 기록한다.",
        },
        "items": atoms,
    }
    OUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    OUT_PATH.write_text(json.dumps(doc, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
