LABEL_CODE = {"①": "01", "②": "02", "③": "03", "④": "04", "⑤": "05", "ㄱ": "ga", "ㄴ": "na", "ㄷ": "da", "ㄹ": "ra", "ㅁ": "ma"}


def today() -> str:
    return datetime.now(ZoneInfo("Asia/Seoul")).date().isoformat()


def load_rows() -> dict[int, list[dict[str, str | None]]]:
    rows: dict[int, list[dict[str, str | None]]] = defaultdict(list)
    for line in REP_BLOCK.strip().splitlines():
        no_text, rep, *rest = line.split("|")
        trap = rest[0].strip() if rest and rest[0].strip() else None
        rows[int(no_text)].append({"rep": rep.strip(), "trap": trap})
    return dict(rows)


def atom_id(item: dict) -> str:
    label = LABEL_CODE.get(item["unitLabel"], item["unitLabel"])
    return f"bupmusa-2025-civil-law-q{item['no']:02d}-{label}"


def group_items(items: list[dict]) -> dict[int, list[dict]]:
    grouped: dict[int, list[dict]] = defaultdict(list)
    for item in items:
        grouped[item["no"]].append(item)
    return dict(grouped)


def basis_for(no: int) -> tuple[str, str, str]:
    return BASIS_OVERRIDES.get(no, DEFAULT_BASIS)


def validate(atoms: list[dict], source_items: list[dict]) -> None:
    if len(atoms) != len(source_items):
        raise ValueError(f"atom count mismatch: atoms={len(atoms)} source={len(source_items)}")
    if len({atom["atomId"] for atom in atoms}) != len(atoms):
        raise ValueError("duplicate atomId")
    banned = ["?", "？", "보기", "옳은", "옳지", "틀린", "①", "②", "③", "④", "⑤"]
    banned_starts = ("위 ", "위의", "위와", "다음")
    for atom in atoms:
        rep = atom["rep"]
        if rep.startswith(banned_starts) or any(pattern in rep for pattern in banned):
            raise ValueError(f"non-atomic wording in {atom['atomId']}: {rep}")
        if re.search(r"\s{2,}", rep):
            raise ValueError(f"double whitespace in {atom['atomId']}: {rep}")
        if atom["sourceVerdict"] == "X" and not atom["sourceTrap"]:
            raise ValueError(f"X source must have trap: {atom['atomId']}")
        if atom["sourceVerdict"] == "O" and atom["sourceTrap"] is not None:
            raise ValueError(f"O source must not have trap: {atom['atomId']}")
        if atom["sourceVerdict"] == "X" and atom["xDependsOn"] != rep:
            raise ValueError(f"X source must depend on normalized O atom: {atom['atomId']}")
        if atom["currentVerdict"] != "O" or atom["a"] != "O":
            raise ValueError(f"completed atom must be O: {atom['atomId']}")


def build_atoms(queue: dict) -> list[dict]:
    grouped = group_items(queue["items"])
    rows = load_rows()
    if set(grouped) != set(rows):
        raise ValueError(f"question coverage mismatch: queue={sorted(grouped)} rows={sorted(rows)}")

    checked_at = today()
    atoms: list[dict] = []
    for no in sorted(grouped):
        items = grouped[no]
        specs = rows[no]
        if len(items) != len(specs):
            raise ValueError(f"row count mismatch for question {no}: queue={len(items)} rows={len(specs)}")
        basis_type, basis_ref, why = basis_for(no)
        for item, spec in zip(items, specs):
            verdict = item.get("originalVerdict")
            if verdict not in {"O", "X"}:
                raise ValueError(f"source verdict is required for {item['unitId']}")
            atom = {
                "atomId": atom_id(item),
                "sourceUnitId": item["unitId"],
                "sourceFamily": item["sourceFamily"],
                "source": item["source"],
                "year": item["year"],
                "round": item["round"],
                "subject": SUBJECT_NAME,
                "no": item["no"],
                "unitType": item["unitType"],
                "unitLabel": item["unitLabel"],
                "sourceQuestionType": item["sourceQuestionType"],
                "officialQuestionAnswer": item["officialQuestionAnswer"],
                "sourceVerdict": verdict,
                "currentVerdict": "O",
                "rep": spec["rep"],
                "a": "O",
                "basisType": basis_type,
                "basisRef": basis_ref,
                "why": why,
                "sourceStatement": item["rawStatement"],
                "sourceTrap": spec["trap"] if verdict == "X" else None,
                "xDependsOn": spec["rep"] if verdict == "X" else None,
                "reviewedAt": checked_at,
                "currentLawCheckedAt": checked_at,
            }
            atoms.append(atom)
    validate(atoms, queue["items"])
    return atoms


def update_subject_index(atom_count: int) -> None:
    if not SUBJECT_INDEX_PATH.exists():
        return
    index = json.loads(SUBJECT_INDEX_PATH.read_text(encoding="utf-8"))
    subjects = index.setdefault("subjects", {})
    target = None
    for key, value in subjects.items():
        text = f"{key} {value.get('subject', '')} {value.get('atomQueue', '')}"
        if SUBJECT_NAME in text:
            target = value
            break
    if target is None:
        target = subjects.setdefault(SUBJECT_NAME, {"subject": SUBJECT_NAME})
    target["completedAtoms"] = str(OUT_PATH)
    target["completedAtomCount"] = atom_count
    target["completedAtomsUpdatedAt"] = today()
    SUBJECT_INDEX_PATH.write_text(json.dumps(index, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


def build() -> Path:
    queue = json.loads(QUEUE_PATH.read_text(encoding="utf-8"))
    atoms = build_atoms(queue)
    doc = {
        "schema": "legal-scrivener/completed-atoms-by-subject/v1",
        "sourceFamily": "법무사시험",
        "examId": queue["examId"],
        "year": queue["year"],
        "round": queue["round"],
        "subject": SUBJECT_NAME,
        "updatedAt": today(),
        "atomPrinciple": "docs/atom_원칙_v001.md",
        "sourceQueue": str(QUEUE_PATH),
        "sourceCount": len(queue["items"]),
        "atomCount": len(atoms),
        "verificationSources": LEGAL_SOURCES,
        "policy": {
            "sourceStatement": "문제 원문 지문은 보존한다.",
            "rep": "화면 출력용 atom은 O인 자기완결식 민법 법리 문장으로 작성한다.",
            "xHandling": "출제 원문이 X인 경우에도 rep는 올바른 O 법리로 정규화하고 sourceTrap에 함정 지점을 기록한다.",
            "basis": "민법 조문, 관련 특별법, 대법원 판례를 문항별 근거 묶음으로 병기한다.",
        },
        "items": atoms,
    }
    OUT_PATH.write_text(json.dumps(doc, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    update_subject_index(len(atoms))
    return OUT_PATH


def main() -> None:
    out = build()
    data = json.loads(out.read_text(encoding="utf-8"))
    by_verdict: dict[str, int] = {}
    by_basis: dict[str, int] = {}
    for item in data["items"]:
        by_verdict[item["sourceVerdict"]] = by_verdict.get(item["sourceVerdict"], 0) + 1
        by_basis[item["basisType"]] = by_basis.get(item["basisType"], 0) + 1
    print(f"atoms={out}")
    print(f"atomCount={data['atomCount']}")
    print("sourceVerdict=" + ", ".join(f"{key}:{value}" for key, value in sorted(by_verdict.items())))
    print("basis=" + ", ".join(f"{key}:{value}" for key, value in sorted(by_basis.items())))
    for sample in data["items"][:3]:
        print(f"{sample['atomId']} {sample['rep']}")


if __name__ == "__main__":
    main()
